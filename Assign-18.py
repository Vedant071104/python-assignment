# Q.1) write the python script to print first N even natural number.
n=int(input("enter even number"))
i=1
while i<=n:
    print(i*2,end=" ")
    i+=1
    
# Q.2) In revesr order.
n=int(input("enter even number"))
while n>=1:
    print(n*2,end=" ")
    n-=1
    
